#Matrices, tienen la misma caracteristica de las listas (se pueden modificar) son datos Mutables
matrix = [[1,2,3],
           [4,5,6],
           [7,8,9]]
print(matrix)
print(matrix[0])

#Tuplas (Tuple) no se puede modificar, son datos inmutables

numbers = (1,2,3,4,5)
print(numbers)
print(type(numbers))
print(numbers[0])
